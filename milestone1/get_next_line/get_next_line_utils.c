#include "get_next_line.h"

size_t	ft_strlen(char *s)
{
	int	i;

	i = 0;
	while (s[i])
	{
		i++;
	}
	return (i);
}

int main ()
{
	char str[]="Hola Mundo";
	int result = ft_strlen(str);
	printf("LETRAS: %d", result);
	return 0;
}